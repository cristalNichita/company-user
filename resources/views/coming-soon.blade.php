        // Set the target date and time (year, month (0-11), day, hours, minutes, seconds)
        var targetDate = new Date("01 January 2024"); // January 1, 2023, 00:00:00

        function updateCountdown() {
            var currentDate = new Date();
            var timeDifference = targetDate - currentDate;

            var days = Math.floor(timeDifference / (1000 * 60 * 60 * 24));
            var hours = Math.floor((timeDifference % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
            var minutes = Math.floor((timeDifference % (1000 * 60 * 60)) / (1000 * 60));
            var seconds =  Math.floor((timeDifference % (1000 * 60)) / 1000);

            if (days < "10") { days = "0" + days; }
            if (hours < "10") { hours = "0" + hours; }
            if (minutes < "10") { minutes = "0" + minutes; }
            if (seconds < "10") { seconds = "0" + seconds; }

            // Update the HTML elements with the calculated values
            $("#days").text(days);
            $("#hours").text(hours);
            $("#minutes").text(minutes);
            $("#seconds").text(seconds);
        }

        // Initial call to display the countdown
        updateCountdown();

        // Update the countdown every 1 minute (adjust as needed)
        setInterval(updateCountdown, 1000); // 1 minute = 60,000 milliseconds // For every second 1000
